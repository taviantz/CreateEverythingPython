# Python lesson. Integer arithmetic: special operations.
# This script demonstrates the calculation of the remainder using integer division and modulo operator.

# Explanation:
# The code calculates the remainder when 62 is divided by 12.
# The modulo operator (%) returns the remainder of the division of the left operand by the right operand.

# Calculate the remainder of 62 divided by 12 using the modulo operator.
remainder = 62 % 12

# Print the remainder to the consule.
print(remainder)

# The expected output is 2 because:
# - When 62 is divided by 12,the quotient is (since 12 * 5 = 60).
# - The remainder is the difference between 62 and 60, which is 2.