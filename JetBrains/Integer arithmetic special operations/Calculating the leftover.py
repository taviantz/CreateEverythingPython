# Calculating the leftover

# Lesson:
# Lesson in Integer arithmetic: special operations. Modulo Operator practice. 
# Discovering number of chocolates left over after distributing equally among 4 children.
# Each integer has been given a variable for clear syntax and clarity.

# Numbers given variables and value for clarity.
total_chocolates = 15
number_children = 4

# Calculating the leftovers. Variables are used with modulo operator. 
# The modulo operator is used to find the remainder of a division operation.
# Use print remainder to find the remainder. 
remainder = total_chocolates % number_children
print(remainder)